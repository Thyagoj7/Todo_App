   private void showJTableTasks(boolean hasTasks){   //Este método decide se vai mostrar a lista de tarefas ou não. //Ele recebe como parametro se as tarefas foram carregadas. Se existe tarefas.
       if(hasTasks){
           //Existem tarefas
           if(jPanelEmptyList.isVisible()){   //verifica se o componente de lista vazia esta visivel na tela
               jPanelEmptyList.setVisible(false);   //Se estiver ele vai esconder
               jPanel5.remove(jPanelEmptyList);   //remove o componente da tela.
           }
           
           jPanel5.add(jScrollPaneTasks);  //Se tiver tarefas vai addcionar o table para dentro do container panel5
           jScrollPaneTasks.setVisible(true);  //Ele vai tornar visivel a tabela(detalhe a table esta dentro do scroll)
           jScrollPaneTasks.setSize(jPanel5.getWidth(), jPanel5.getHeight()); //Vai ajudar a tabela de acordo com o tamanho do jPanel5
       }else{ // Se não tiver tarefas
           if(jScrollPaneTasks.isVisible()) {  //Esconde o scroll que contem as tarefas
              jScrollPaneTasks.setVisible(false);      //Esconcer
              jPanel5.remove(jScrollPaneTasks);     //Remover da tela
           }
           
           jPanel5.add(jPanelEmptyList);   //Adcionar o EmptyList
           jPanelEmptyList.setVisible(true);  //tornalo visivel
           jPanelEmptyList.setSize(jPanel5.getWidth(), jPanel5.getHeight()); //Ajustar o tamanho
       }
   }
