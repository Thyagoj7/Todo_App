    private void jTableTasksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTasksMouseClicked
        // TODO add your handling code here:
        int rowIndex = jTableTasks.rowAtPoint(evt.getPoint()); //Descobrir a linha a partir de um ponto na tela. Pegamos o ponto através do evt que é um obejto que é passado através do evento.
        int columnIndex = jTableTasks.columnAtPoint(evt.getPoint()); //Aqui faz a mesma coisa mas com colunas
        
        Task task = taskModel.getTasks().get(rowIndex); //pegamos o objeto tarefas que esta dentro do taskmovel / o swith vai usar a variavel task criada aqui.

        switch(columnIndex){ //Este swith só vai agir na coluna 3 ou seja na coluna que usa o tarefa completa
            case 3:
                taskcontroller.update(task);
            break;
            case 4:
                break;
            case 5:
                taskcontroller.removeById(task.getId());
                taskModel.getTasks().remove(task);
                
                int projectIndex = jListProjects.getSelectedIndex();
                Project project = (Project) projectsModel.get(projectIndex);
                loadTasks(project.getId());
                
                break;
            
        }
    }//GEN-LAST:event_jTableTasksMouseClicked

